#!/usr/bin/env node
import { spawn } from "node:child_process";
import { existsSync, mkdirSync, mkdtempSync, rmSync, writeFileSync } from "node:fs";
import { randomBytes } from "node:crypto";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "../..");
const pnpmBin = process.platform === "win32" ? "pnpm.cmd" : "pnpm";
const providedDataDir = process.env.PAPERCLIP_E2E_DATA_DIR?.trim();
const dataDir = providedDataDir || mkdtempSync(path.join(tmpdir(), "paperclip-playwright-e2e-"));
const port = process.env.PAPERCLIP_E2E_PORT?.trim() || "3100";
const deploymentMode = process.env.PAPERCLIP_E2E_DEPLOYMENT_MODE?.trim() || "local_trusted";
const deploymentExposure = process.env.PAPERCLIP_E2E_DEPLOYMENT_EXPOSURE?.trim() || "private";
const serverHost = process.env.PAPERCLIP_E2E_HOST?.trim() || "127.0.0.1";
const publicBaseUrl = process.env.PAPERCLIP_E2E_PUBLIC_URL?.trim() || `http://localhost:${port}`;
const configuredAllowedHostnames = (process.env.PAPERCLIP_E2E_ALLOWED_HOSTNAMES?.trim() || "")
  .split(",")
  .map((hostname) => hostname.trim())
  .filter(Boolean);
const allowedHostnames =
  deploymentMode === "authenticated" && deploymentExposure === "private"
    ? Array.from(new Set(["localhost", "127.0.0.1", ...configuredAllowedHostnames]))
    : configuredAllowedHostnames;
const configPath = path.join(dataDir, "instances", "default", "config.json");
const instanceRoot = path.dirname(configPath);
const shouldCleanup = !providedDataDir;

console.log(`[paperclip-playwright] data dir: ${dataDir}`);
console.log(`[paperclip-playwright] port: ${port}`);
console.log(`[paperclip-playwright] deployment mode: ${deploymentMode}/${deploymentExposure}`);

function writeConfig() {
  mkdirSync(instanceRoot, { recursive: true });
  mkdirSync(path.join(instanceRoot, "db"), { recursive: true });
  mkdirSync(path.join(repoRoot, ".log"), { recursive: true });
  mkdirSync(path.join(instanceRoot, "data", "storage"), { recursive: true });
  mkdirSync(path.join(instanceRoot, "data", "backups"), { recursive: true });
  mkdirSync(path.join(instanceRoot, "secrets"), { recursive: true });

  const now = new Date().toISOString();
  const config = {
    $meta: {
      version: 1,
      updatedAt: now,
      source: "onboard",
    },
    database: {
      mode: "embedded-postgres",
      embeddedPostgresDataDir: path.join(instanceRoot, "db"),
      embeddedPostgresPort: 54329,
      backup: {
        enabled: true,
        intervalMinutes: 60,
        retentionDays: 30,
        dir: path.join(instanceRoot, "data", "backups"),
      },
    },
    logging: {
      mode: "file",
      logDir: path.join(repoRoot, ".log"),
    },
    server: {
      deploymentMode,
      exposure: deploymentExposure,
      host: serverHost,
      port: Number(port),
      allowedHostnames,
      serveUi: true,
    },
    auth: {
      baseUrlMode: "auto",
      disableSignUp: false,
    },
    storage: {
      provider: "local_disk",
      localDisk: {
        baseDir: path.join(instanceRoot, "data", "storage"),
      },
      s3: {
        bucket: "paperclip",
        region: "us-east-1",
        prefix: "",
        forcePathStyle: false,
      },
    },
    secrets: {
      provider: "local_encrypted",
      strictMode: false,
      localEncrypted: {
        keyFilePath: path.join(instanceRoot, "secrets", "master.key"),
      },
    },
  };

  const agentJwtSecret = randomBytes(32).toString("hex");
  const betterAuthSecret = randomBytes(32).toString("hex");
  const envFile = [
    "# Paperclip environment variables",
    "# Generated by playwright e2e webserver launcher",
    `PAPERCLIP_AGENT_JWT_SECRET=${agentJwtSecret}`,
    `BETTER_AUTH_SECRET=${betterAuthSecret}`,
    `PAPERCLIP_SECRETS_PROVIDER=local_encrypted`,
    `PAPERCLIP_SECRETS_STRICT_MODE=false`,
    `PAPERCLIP_SECRETS_MASTER_KEY_FILE=${path.join(instanceRoot, "secrets", "master.key")}`,
    "",
  ].join("\n");

  writeFileSync(configPath, `${JSON.stringify(config, null, 2)}\n`, { mode: 0o600 });
  writeFileSync(path.join(instanceRoot, ".env"), envFile, { mode: 0o600 });
}

function cleanup() {
  if (!shouldCleanup) return;
  try {
    rmSync(dataDir, { force: true, recursive: true });
  } catch {
    // Ignore cleanup failures; the test run already finished.
  }
}

let serverChild;
let shuttingDown = false;

function runCommand(command, args) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: repoRoot,
      env: process.env,
      shell: process.platform === "win32",
      stdio: "inherit",
    });

    child.on("error", reject);
    child.on("exit", (code, signal) => {
      if (signal) {
        reject(new Error(`${command} ${args.join(" ")} exited from signal ${signal}`));
        return;
      }
      if (code !== 0) {
        reject(new Error(`${command} ${args.join(" ")} failed with exit code ${code ?? 1}`));
        return;
      }
      resolve();
    });
  });
}

async function ensureRequiredPluginBuilds() {
  if (process.env.PAPERCLIP_E2E_PREBUILD_REQUIRED_PLUGINS === "false") {
    return;
  }

  const requiredBuilds = [
    {
      manifestPath: path.join(
        repoRoot,
        "packages",
        "plugins",
        "plugin-execution-improvement",
        "dist",
        "src",
        "manifest.js",
      ),
      filter: "@paperclipai/plugin-execution-improvement",
    },
    {
      manifestPath: path.join(
        repoRoot,
        "packages",
        "plugins",
        "plugin-skills-system",
        "dist",
        "src",
        "manifest.js",
      ),
      filter: "@paperclipai/plugin-skills-system",
    },
  ];

  for (const plugin of requiredBuilds) {
    if (existsSync(plugin.manifestPath)) {
      continue;
    }

    console.log(`[paperclip-playwright] building required plugin: ${plugin.filter}`);
    await runCommand(pnpmBin, ["--filter", plugin.filter, "build"]);
  }
}

function stop(signal) {
  if (shuttingDown) return;
  shuttingDown = true;
  if (serverChild) {
    serverChild.kill(signal);
  }
}

process.on("SIGINT", () => stop("SIGINT"));
process.on("SIGTERM", () => stop("SIGTERM"));

writeConfig();
await ensureRequiredPluginBuilds();

serverChild = spawn(
  pnpmBin,
  ["--filter", "@paperclipai/server", "dev"],
  {
    cwd: repoRoot,
    env: {
      ...process.env,
      PORT: port,
      HOST: serverHost,
      PAPERCLIP_CONFIG: configPath,
      PAPERCLIP_HOME: dataDir,
      PAPERCLIP_MIGRATION_AUTO_APPLY: "true",
      PAPERCLIP_PUBLIC_URL: publicBaseUrl,
      DATABASE_URL: "",
    },
    shell: process.platform === "win32",
    stdio: "inherit",
  },
);

serverChild.on("error", (error) => {
  cleanup();
  throw error;
});

serverChild.on("exit", (code, signal) => {
  cleanup();
  if (signal) {
    process.exitCode = signal === "SIGINT" ? 130 : 143;
    return;
  }
  process.exitCode = code ?? 0;
});
